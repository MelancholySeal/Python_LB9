#!/usr/bin/env python3
# -*- coding: utf-8 -*-

if __name__ == '__main__':

    # Создание и исходное заполнение словаря
    school = {
        '1а': 25,
        '1б': 28,
        '2б': 30,
        '6а': 24,
        '7в': 27,
        # ... добавьте другие классы
    }

    # Вывод текущего состояния словаря
    print("Исходный словарь:")
    print(school)

    total_students = sum(school.values())
    print("\nОбщее количество учащихся в школе:", total_students)

    # а) Изменение количества учащихся в одном из классов
    school['1а'] = 20

    # б) Добавление нового класса
    school['8г'] = 29

    # с) Удаление класса
    if '2б' in school:
        del school['2б']

    # Вывод измененного состояния словаря
    print("\nИзмененный словарь:")
    print(school)

    # Вычисление общего количества учащихся в школе
    total_students = sum(school.values())
    print("\nОбщее количество учащихся в школе:", total_students)
